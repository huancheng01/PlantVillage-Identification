# 原始字典
imagefolder_list = {'0': 0, '1': 1, '10': 2, '11': 3, '12': 4, '13': 5, '14': 6, '15': 7, '16': 8, '17': 9, '18': 10, '19': 11, '2': 12, '20': 13, '21': 14, '22': 15, '23': 16, '24': 17, '25': 18, '26': 19, '27': 20, '28': 21, '29': 22, '3': 23, '30': 24, '31': 25, '32': 26, '33': 27, '34': 28, '35': 29, '36': 30, '37': 31, '4': 32, '5': 33, '6': 34, '7': 35, '8': 36, '9': 37}

# 调换键和值
swapped_dict = {v: k for k, v in imagefolder_list.items()}

# 使用字典推导式将所有值从字符串转换为整数
swapped_dict = {key: int(value) for key, value in swapped_dict.items()}

# 打印交换后的字典
print(swapped_dict)
# {v: k for k, v in imagefolder_list.items()}：字典推导式，遍历原字典的每个键值对，将原来的值 (v) 作为新的键，将原来的键 (k) 作为新的值。
#
# 这种方法非常直接并且适用于任何字典，前提是字典中的值是唯一的。如果字典中有重复的值，调换键值后，后面的键会覆盖前面的键。